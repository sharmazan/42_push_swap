#include <stdlib.h>

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*p;

	if (n == 0)
		return (s);
	i = 0;
	p = (char *)s;
	while (i < n)
		p[i++] = (unsigned char)c;
	return (s);
}
